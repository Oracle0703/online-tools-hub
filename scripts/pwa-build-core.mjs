import { createHash } from "node:crypto";
import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const cacheableExtensions = new Set([
  ".avif",
  ".css",
  ".gif",
  ".html",
  ".ico",
  ".jpeg",
  ".jpg",
  ".js",
  ".json",
  ".png",
  ".svg",
  ".txt",
  ".wasm",
  ".webmanifest",
  ".webp",
  ".woff",
  ".woff2",
  ".xml",
]);

export function normalizeBasePath(value) {
  const pathname = String(value || "/").split(/[?#]/u, 1)[0] ?? "/";
  const normalized = `/${pathname}`.replace(/\/{2,}/gu, "/");
  const stripped = normalized.replace(/^\/+|\/+$/gu, "");
  return stripped ? `/${stripped}/` : "/";
}

export function filePathToPublicUrl(relativePath, basePath) {
  const normalizedBase = normalizeBasePath(basePath);
  const normalizedFile = relativePath.split(path.sep).join("/");

  if (normalizedFile === "index.html") return normalizedBase;
  if (normalizedFile.endsWith("/index.html")) {
    return `${normalizedBase}${normalizedFile.slice(0, -"index.html".length)}`;
  }
  return `${normalizedBase}${normalizedFile}`;
}

export function shouldPrecache(relativePath) {
  const normalized = relativePath.split(path.sep).join("/");
  if (
    normalized === "service-worker.js" ||
    normalized.endsWith(".map") ||
    normalized.startsWith("playwright-report/") ||
    normalized.startsWith("test-results/")
  ) {
    return false;
  }
  return cacheableExtensions.has(path.extname(normalized));
}

async function collectFiles(directory, root = directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const absolutePath = path.join(directory, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await collectFiles(absolutePath, root)));
    } else {
      files.push(path.relative(root, absolutePath));
    }
  }

  return files;
}

export async function createPrecacheManifest(distDirectory, basePath) {
  const relativePaths = (await collectFiles(distDirectory))
    .filter(shouldPrecache)
    .sort((left, right) => left.localeCompare(right, "en"));
  const hash = createHash("sha256");

  for (const relativePath of relativePaths) {
    hash.update(relativePath);
    hash.update("\0");
    hash.update(await readFile(path.join(distDirectory, relativePath)));
    hash.update("\0");
  }

  return {
    version: hash.digest("hex").slice(0, 16),
    urls: relativePaths.map((relativePath) =>
      filePathToPublicUrl(relativePath, basePath),
    ),
  };
}

export function createServiceWorkerSource({ basePath, version, urls }) {
  const normalizedBase = normalizeBasePath(basePath);
  const offlineUrl = `${normalizedBase}offline.html`;
  if (!urls.includes(offlineUrl)) {
    throw new Error(`离线回退页未进入预缓存：${offlineUrl}`);
  }

  return `/* Generated by scripts/generate-service-worker.mjs. */
const CACHE_PREFIX = "online-tools-hub-static-";
const CACHE_NAME = CACHE_PREFIX + ${JSON.stringify(version)};
const BASE_PATH = ${JSON.stringify(normalizedBase)};
const OFFLINE_URL = ${JSON.stringify(offlineUrl)};
const PRECACHE_URLS = Object.freeze(${JSON.stringify(urls, null, 2)});
const PRECACHE_SET = new Set(PRECACHE_URLS);

self.addEventListener("install", (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) =>
      cache.addAll(
        PRECACHE_URLS.map(
          (url) =>
            new Request(url, { cache: "reload", credentials: "same-origin" }),
        ),
      ),
    ),
  );
});

self.addEventListener("message", (event) => {
  if (event.data?.type === "SKIP_WAITING") self.skipWaiting();
});

self.addEventListener("activate", (event) => {
  event.waitUntil(
    caches
      .keys()
      .then((names) =>
        Promise.all(
          names
            .filter(
              (name) => name.startsWith(CACHE_PREFIX) && name !== CACHE_NAME,
            )
            .map((name) => caches.delete(name)),
        ),
      )
      .then(() => self.clients.claim()),
  );
});

function requestDetails(request) {
  if (request.method !== "GET") return null;

  const url = new URL(request.url);
  if (
    url.origin !== self.location.origin ||
    !url.pathname.startsWith(BASE_PATH) ||
    (url.protocol !== "https:" && url.protocol !== "http:")
  ) {
    return null;
  }

  return { url, cacheKey: url.pathname };
}

async function respondToNavigation(request, cacheKey) {
  try {
    return await fetch(request);
  } catch {
    const cache = await caches.open(CACHE_NAME);
    if (PRECACHE_SET.has(cacheKey)) {
      const cachedRoute = await cache.match(cacheKey);
      if (cachedRoute) return cachedRoute;
    }
    return (await cache.match(OFFLINE_URL)) ?? Response.error();
  }
}

function normalizedNavigationCacheKey(pathname) {
  if (PRECACHE_SET.has(pathname)) return pathname;
  if (pathname === BASE_PATH + "index.html") return BASE_PATH;
  const withTrailingSlash = pathname.endsWith("/") ? pathname : pathname + "/";
  return PRECACHE_SET.has(withTrailingSlash) ? withTrailingSlash : pathname;
}

async function respondWithPrecache(request, cacheKey) {
  const cache = await caches.open(CACHE_NAME);
  return (await cache.match(cacheKey)) ?? fetch(request);
}

self.addEventListener("fetch", (event) => {
  const details = requestDetails(event.request);
  if (!details) return;

  if (event.request.mode === "navigate") {
    event.respondWith(
      respondToNavigation(
        event.request,
        normalizedNavigationCacheKey(details.cacheKey),
      ),
    );
    return;
  }

  // Only build-time, query-free allowlisted assets are intercepted. Runtime
  // requests are never written to Cache Storage.
  if (
    details.url.search === "" &&
    PRECACHE_SET.has(details.cacheKey)
  ) {
    event.respondWith(respondWithPrecache(event.request, details.cacheKey));
  }
});
`;
}

export async function generateServiceWorker({ distDirectory, basePath }) {
  const manifest = await createPrecacheManifest(distDirectory, basePath);
  const source = createServiceWorkerSource({
    basePath,
    version: manifest.version,
    urls: manifest.urls,
  });
  const destination = path.join(distDirectory, "service-worker.js");
  await writeFile(destination, source, "utf8");
  return { ...manifest, destination };
}
